package SistemaEscolar;

public class Aluno {

    public String nome;
    public int idade;
    public int matricula;


    // Construtor com parametros "nome, idade e matricula"
    public Aluno(String nome, int idade, int matricula){
        this.nome = nome;
        this.idade = idade;
        this.matricula = matricula;
    }

    // metodos
    public String getNome() {
        return nome;
    }

    public int getIdade() {
        return idade;
    }

    public int getMatricula() {
        return matricula;
    }












}
